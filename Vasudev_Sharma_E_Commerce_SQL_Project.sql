-- Q. DISPLAY PRODUCT PRICE CATEGORY USING CASE.
-- (LESS THAN ₹1000 → LOW PRICE, ₹1000–₹10000 → MEDIUM PRICE, ABOVE ₹10000 → HIGH PRICE) 
SELECT PRODUCT_ID , PRODUCT_NAME , PRICE , 
CASE
WHEN PRICE < 1000 THEN 'LOW PRICE'
WHEN PRICE BETWEEN 1000 AND 10000 THEN 'MEDIUM PRICE'
ELSE 'HIGH PRICE'
END AS PRICE_CATEGORY
FROM PRODUCTS; 
-- Q. DISPLAY STOCK STATUS USING CASE.
-- (STOCK < 20 → LOW STOCK, OTHERWISE → AVAILABLE)
SELECT PRODUCT_NAME , STOCK ,
CASE WHEN STOCK < 20 THEN 'LOW STOCK'
ELSE 'AVAILABLE'
END AS STOCK_CATEGORY
FROM PRODUCTS ; 
-- Q. DISPLAY CUSTOMER TYPE USING CASE BASED ON NUMBER OF ORDERS.
SELECT C.CUSTOMER_ID , CUSTOMER_NAME ,
COUNT(O.ORDER_ID) AS TOTAL_ORDER ,
CASE WHEN COUNT(O.ORDER_ID) > 5 THEN 'REGULAR CUSTOMER'
WHEN COUNT(O.ORDER_ID) BETWEEN 2 AND 5 THEN 'RETURNING CUSTOMER'
ELSE 'NEW CUSTOMER'
END AS CUSTOMER_TYPE
FROM CUSTOMERS AS C 
LEFT JOIN ORDERS AS O 
ON C.CUSTOMER_ID = O.CUSTOMER_ID 
GROUP BY C.CUSTOMER_ID , CUSTOMER_NAME ;
-- Q. DISPLAY PRODUCT DISCOUNT ELIGIBILITY USING CASE.
SELECT PRODUCT_ID , PRODUCT_NAME , PRICE , 
CASE WHEN PRICE > 3000 THEN 'DISCOUNT ELIGIBLE'
ELSE 'NOT ELIGIBLE'
END AS DISCOUNT_STATUS
FROM PRODUCTS ;
-- Q. DISPLAY PAYMENT STATUS USING CASE EXPRESSION.
SELECT ORDER_ID , PAYMENT_MODE , 
CASE WHEN PAYMENT_MODE = 'CASH' THEN 'NICE CUSTOMER'
WHEN PAYMENT_MODE = 'UPI' THEN 'EDUCATED CUSTOMER'
WHEN PAYMENT_MODE = 'CARD' THEN 'GOOD CUSTOMER'
ELSE 'OTHER PAYMENT MTHOD'
END AS PAYMENT_STATUS
FROM ORDERS ; 
-- Q. DISPLAY CUSTOMER PURCHASE LEVEL USING CASE.
SELECT C.CUSTOMER_ID , CUSTOMER_NAME , SUM(OD.QUANTITY * P.PRICE) AS TOTAL_PURCHASE,
CASE WHEN SUM(OD.QUANTITY * P.PRICE) > 50000 THEN 'PREMIUM CUSTOMER'
WHEN SUM(OD.QUANTITY * P.PRICE) BETWEEN 20000 AND 50000 THEN 'REGULAR CUSTOMER'
ELSE 'NORMAL CUSTOMER'
END AS PURCHASE_LEVEL
FROM CUSTOMERS AS C 
JOIN ORDERS AS O 
ON C.CUSTOMER_ID = O.CUSTOMER_ID 
JOIN ORDER_DETAILS AS OD 
ON OD.ORDER_ID = O.ORDER_ID 
JOIN PRODUCTS AS P 
ON P.PRODUCT_ID = OD.PRODUCT_ID 
GROUP BY C.CUSTOMER_ID , CUSTOMER_NAME ;   
