-- Q. FIND THE SECOND MOST EXPENSIVE PRODUCT USING DENSE_RANK().
SELECT PRODUCT_ID , PRODUCT_NAME , PRICE 
FROM(
SELECT PRODUCT_ID , PRODUCT_NAME , PRICE , 
DENSE_RANK() OVER(ORDER BY PRICE DESC) AS PRICE_RANK
FROM PRODUCTS
) AS T 
WHERE PRICE_RANK = 2 ;
